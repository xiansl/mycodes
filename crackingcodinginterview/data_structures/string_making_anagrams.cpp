using namespace std;

int number_needed(string a, string b) {
    int c1[26] = {0}, c2[26] = {0}, ct=0;
    for(char& c : a) c1[c-'a']++;
    for(char& c : b) c2[c-'a']++;
    for(int i=0; i<26; i++) ct += abs(c1[i]-c2[i]);
    return ct;
}

int main(){
    string a;
    cin >> a;
    string b;
    cin >> b;
    cout << number_needed(a, b) << endl;
    return 0;
}
